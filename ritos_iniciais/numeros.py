#  Operações Matemáticas com números Inteiros

# Soma
print(1 + 2)
print(1.0 + 3)

# Diferença
print(2 - 1)

# Multiplicação
print(2 * 3)

# Divisão (a dupla "//" é para retornar um inteiro)
print(6 // 2)

# Potênciação
print(2 ** 3)

# Módulo (Resto da divisão)
print(10 % 3)


# Operações Matemáticas com números float


print(5.3 + 9.4)

print(6 + 7.0)

print(4 + 4)

print(4 / 2)


print(8 / 5.0)

print(4 // 3.0)

# Nós podemos converter os números


float(5)

int(6.70)

int(1.5)

# Nós podemos converter Binário e Hexadecimal


hex(777)

hex(372)

bin(5)

bin(171)

# Algumas funções:


# Retorna o valor absoluto
abs(-87)


# Retorna o valor com arredondamento
round(2.9871, 2)

# Potênciação (2)
print(pow(2, 3))


# Uma função especial que retorna o tipo de objeto ou classe.

print(type(4.2))

print(type(5.0))

variavel = 'O que eu sou?'
print(type(variavel))